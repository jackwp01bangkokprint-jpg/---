// Robust function to parse license plate from vehicle name
function extractPlate(name: string): string {
  // Matches typical Thai license plates like "5ขผ9463", "6 ขค 7190", "ฎ 3644", "70-2052", "700-6491"
  const match = name.match(/(\d?\s?[ก-ฮ]{1,2}\s?\d{1,4})|(\d{2,3}-\d{4})/);
  if (match) {
    return match[0].replace(/\s+/g, ''); // Clear multiple spaces
  }
  return 'EV-CAR';
}

